# construtor da classe

class Romance:

    def __init__(self, nome, ano, autor, editora):
        self.__nome = nome
        self.__ano = ano
        self.__autor = autor
        self.__editora = editora

# get e set dos atributos

    def getNome(self):
        return self.__nome

    def getAno(self):
        return self.__ano

    def getAutor(self):
        return self.__autor

    def getEditora(self):
        return self.__editora

    def setNome(self, nome):
        self.__nome = nome
        return self.__nome

    def setAno(self, ano):
        self.__ano = ano
        return self.__ano

    def setAutor(self, autor):
        self.__autor = autor
        return self.__autor

    def setEditora(self, editora):
        self.__editora = editora
        return self.__editora

# metódo de descrição do livro

    def mostrarinfo_romance(self):
        return f"Romance: {self.__nome} ({self.__ano})\nAutor: {self.__autor}\nEditora: {self.__editora}"

